console.log("Odd Numbers upto 10");
for(let i=1;i<=10;i=i+2)
{
    console.log(i);
}
console.log("Even Numbers upto 10");
for(let j=0;j<=10;j=j+2) // 0 is even number
{
    console.log(j);
}
console.log("odd number using while loop");
let k=1
while(k<=10)
{
    console.log(k);
    k=k+2;
}
console.log("Even number using while loop");
let l=0;
while(l<=10)
{
    console.log(l);
    l=l+2;
    
}
